export const ICON_OPTIONS = ICON_KEYS.map((key) => {
  const C = (AegisIcons as Record<string, ComponentType>)[key];
  const label = key.replace(/^Lf/, "");
  return {
    value: key,
    label,
    body: C ? (
      <ActionListBody
        leading={
          <Icon>
            <C />
          </Icon>
        }
      >
        {label}
      </ActionListBody>
    ) : (
      label
    ),
  };
});

export const renderAegisIcon = (name: string): React.ReactElement | null => {
  const C = (AegisIcons as Record<string, ComponentType>)[name];
  return C ? React.createElement(C) : null;
};

const FallbackIcon: ComponentType = () => null;

export const getAegisIconComponent = (name: string): ComponentType => {
  return (AegisIcons as Record<string, ComponentType>)[name] ?? FallbackIcon;
};
